// My age as a variable //
const myAge =22;

// Early years variable //
let earlyYears = 2;

earlyYears *=10.5;
console.log(earlyYears);

// My age subtracted by two equal to variable laterYears //
let laterYears = myAge - 2;

// My later years represented in dog years //
laterYears*=4;
console.log(laterYears);

// My age in dog years //
let myAgeInDogYears = earlyYears + laterYears;
console.log(myAgeInDogYears);

// My name written as a string //
myName = 'James Barrington'.toLowerCase();

console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`);